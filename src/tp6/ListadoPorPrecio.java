    private void jBMinMaxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBMinMaxActionPerformed
        borrarFilas();
        try{
            Double min = Double.parseDouble(jTMinimo.getText());
            Double max = Double.parseDouble(jTMaximo.getText());
            if(jTMinimo.getText() == null || jTMaximo.getText() == null){
                JOptionPane.showMessageDialog(this, "No deben haber campos vacios");
            }else{
                for(Producto producto : MenuGeneral.productList){
                    if(producto.getPrecio()>= min && producto.getPrecio() <= max){
                        tabla.addRow(new Object[]{producto.getCodigo(), producto.getDescripcion(), producto.getPrecio(), producto.getStock()});
                    }
                }
            }
            
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Solo se aceptan numeros");
        }
    }//GEN-LAST:event_jBMinMaxActionPerformed


        private void borrarFilas(){
            int indice = tabla.getRowCount()-1;
            for (int i = indice;i>=0;i--){
                tabla.removeRow(i);
            }
        }
        
