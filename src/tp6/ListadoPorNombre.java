    private void jTNombreListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTNombreListaActionPerformed
        String buscar = jTNombreLista.getText().trim().toLowerCase();
        borrarFilas();
        for(Producto producto : MenuGeneral.productList){
            if("".equals(buscar)){
                borrarFilas();
            }
            else if(producto.getDescripcion().startsWith(buscar)){
                tabla.addRow(new Object[]{producto.getCodigo(), producto.getDescripcion(), producto.getPrecio(), producto.getStock()});
            }
        }
    }//GEN-LAST:event_jTNombreListaActionPerformed

    private void borrarFilas(){
        int indice = tabla.getRowCount()-1;
        for (int i = indice;i>=0;i--){
            tabla.removeRow(i);
        }
    }

